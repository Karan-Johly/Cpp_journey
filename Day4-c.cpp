#include<iostream>
using namespace std;
int main()
{
    char ch;
    short s;
    int i;
    long l;
    float f;
    double d;

    cout<<"No of bytes allocated to char datatype :"<<sizeof(ch)<<endl;
    cout<<"No of bytes allocated to short datatype :"<<sizeof(s)<<endl;
    cout<<"No of bytes allocated to int datatype :"<<sizeof(i)<<endl;
    cout<<"No of bytes allocated to long datatype :"<<sizeof(l)<<endl;
    cout<<"No of bytes allocated to float datatype :"<<sizeof(f)<<endl;
    cout<<"No of bytes allocated to double datatype :"<<sizeof(d)<<endl;
}
